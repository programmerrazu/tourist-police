package com.tourist.police.model;

import java.io.Serializable;

public class HomeInfo implements Serializable {

    private int id;
    private String name;
    private int image;

    public HomeInfo() {
    }

    public HomeInfo(int id, String name, int image) {
        this.id = id;
        this.name = name;
        this.image = image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}